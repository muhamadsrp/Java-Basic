public class increment_dan_decrement {
    
    public static void main(String args[]){
        
        int i = 4;
        
        
        System.out.println("i = "+ i);
        System.out.println("++i = "+ ++i);
        System.out.println("i++ = "+ i++);
        System.out.println("--i = "+ --i);
        System.out.println("i-- = "+ i--);
                
    }
    
}
