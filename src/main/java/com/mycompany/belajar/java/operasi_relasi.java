public class operasi_relasi {
    
    public static void main(String args[]){
        
        int a = 4;
        int b = 3;
        
        System.out.println(a == b); // Sama dengan
        System.out.println(a != b); // Tidak sama dengan
        System.out.println(a > b); // Lebih besar
        System.out.println(a < b); // Lebih kecil
        System.out.println(a >= b); // Lebih besar dan sama dengan
        System.out.println(a <= b); // Lebih kecil dan sama dengan
        
    }
    
}
